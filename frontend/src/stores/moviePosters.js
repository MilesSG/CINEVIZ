/* 获取电影海报URL */
export function getMoviePosterUrl(title) {
  // 创建文件名映射，解决特殊字符问题
  const fileNameMap = {
    '蜘蛛侠：英雄远征': '蜘蛛侠_英雄远征'
  };
  
  // 处理特殊文件名
  const fixedTitle = fileNameMap[title] || title;
  
  // 首先尝试获取本地海报路径
  const localPosterUrl = `/posters/${fixedTitle}.jpg`;
  
  // 如果本地海报加载失败，尝试使用备用URL
  const backupUrl = backupImageUrls[title];
  if (backupUrl) {
    return backupUrl;
  }
  
  // 如果都不存在，使用默认占位符图片
  return "/posters/default-poster.jpg";
}

